    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        final String ERROR_TITULO = "Un problema ha ocurrido...";
        
        String codigo = txtCodigo.getText();
        Sucursal sucursal = null;
        try {
            sucursal = Sucursales.obtener(codigo);           
            
        } catch (SQLException ex) {
            Logger.getLogger(IFrmModificarSucursal.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(sucursal != null) {
            if(sucursal.getCodigo().compareTo("00") != 0) {
                txtNombre.setText(String.valueOf(sucursal.getNombre()));
                txtCiudad.setText(String.valueOf(sucursal.getCiudad()));
                txtDireccion.setText(String.valueOf(sucursal.getDireccion()));
                activar(true);
            } else {
                JOptionPane.showMessageDialog(this, "El c칩digo no existe", ERROR_TITULO, 1);
                limpiar();
                activar(false);
            }
        } else {
            JOptionPane.showMessageDialog(this, "El c칩digo no es valido", ERROR_TITULO, 0);
            limpiar();
            activar(false);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        final String CORRECTO_TITULO = "Operaci칩n realizada";
        try {
            Sucursales.actualizarNombre(txtCodigo.getText(),txtNombre.getText());
            Sucursales.actualizarCiudad(txtCodigo.getText(),txtCiudad.getText());
            Sucursales.actualizarDireccion(txtCodigo.getText(),txtDireccion.getText());
            
            JOptionPane.showMessageDialog(this, "Se actualizo correctamente.", CORRECTO_TITULO, JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(IFrmModificarSucursal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed
    
    private void activar(boolean estado) {
        txtCodigo.setEnabled(!estado);
        txtNombre.setEnabled(estado);
        txtCiudad.setEnabled(estado);
        txtDireccion.setEnabled(estado);
        btnBuscar.setEnabled(!estado);
        btnActualizar.setEnabled(estado);
    }
    
    private void limpiar() {
        txtCodigo.setText(null);
        txtNombre.setText(null);
        txtCiudad.setText(null);
        txtDireccion.setText(null);
    }

