    private void btnPaso2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPaso2ActionPerformed
        String dni = txtDNI.getText();
        String nombres = txtNombres.getText();
        String apellidoPaterno = txtApellidoPaterno.getText();
        String apellidoMaterno = txtApellidoMaterno.getText();
        String ciudad = txtCiudad.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        String email = txtEmail.getText();
        
        if (!verificarRestricciones()) return;
        
        Cliente c = new Cliente();
        c.setDni(dni);
        c.setNombre(nombres);
        c.setApellidoPaterno(apellidoPaterno);
        c.setApellidoMaterno(apellidoMaterno);
        c.setCiudad(ciudad);
        c.setDireccion(direccion);
        c.setTelefono(telefono);
        c.setEmail(email);
        
        RegistroPaso2 reg2 = RegistroPaso2.getInstance(c);
        this.setVisible(false);
        reg2.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnPaso2ActionPerformed
    
    private boolean verificarRestricciones(){
        String dni = txtDNI.getText();
        String nombres = txtNombres.getText();
        String apellidoPaterno = txtApellidoPaterno.getText();
        String apellidoMaterno = txtApellidoMaterno.getText();
        String ciudad = txtCiudad.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        String email = txtEmail.getText();
        
        if (dni.isBlank()){
            JOPFallo("DNI");
            return false;
        }
        
        if (dni.length() != 8){
            JOptionPane.showMessageDialog(this, "Paso 1 del Registro | El DNI tiene que tener 8 caracteres.", "Un problema ha ocurrido...", JOptionPane.ERROR_MESSAGE);
            return false;
        }
            
        if (nombres.isBlank()){
            JOPFallo("nombres");
            return false;
        }
        if (apellidoPaterno.isBlank()){
            JOPFallo("apellido paterno");
            return false;
        }
        if (apellidoMaterno.isBlank()){
            JOPFallo("apellido materno");
            return false;
        }
        if (ciudad.isBlank()){
            JOPFallo("ciudad");
            return false;
        }
        if (direccion.isBlank()){
            JOPFallo("dirección");
            return false;
        }
        if (telefono.isBlank()){
            JOPFallo("teléfono");
            return false;
        }
        if (email.isBlank()){
            JOPFallo("email");
            return false;
        }
        
        return true;
    }
    
    private void JOPFallo(String campo){
        JOptionPane.showMessageDialog(this, "Paso 1 del Registro | El campo de " + campo + " no puede estar vacío.", "Un problema ha ocurrido...", JOptionPane.ERROR_MESSAGE);
    }
    
